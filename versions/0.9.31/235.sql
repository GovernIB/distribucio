UPDATE DIS_REGLA r SET BACKOFFICE_DESTI_ID = 
( 
	SELECT ID 
	FROM DIS_BACKOFFICE b
	WHERE ROWNUM = 1
	 	AND r.TIPUS_BACKOFFICE = b.TIPUS 
	 	AND (r.BACKOFFICE_CODI = b.CODI OR (r.BACKOFFICE_CODI IS NULL AND r.NOM = b.NOM ))
	 	AND r.NOM = b.NOM 
	 	AND r.URL = b.URL 
);
