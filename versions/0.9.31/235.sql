	FROM (
		SELECT DISTINCT
		    NVL(r.TIPUS_BACKOFFICE, 'DISTRIBUCIO') TIPUS ,
		    NVL(r.BACKOFFICE_CODI, 'Backoffice_'||rownum) CODI,
		    NVL(URL, 'http://camp.obligatori') URL,
		    USUARI,
		    CONTRASENYA,
		    INTENTS,
		    TEMPS_ENTRE_INTENTS,
		    ENTITAT_ID
		FROM DIS_REGLA r
		WHERE r.TIPUS = 'BACKOFFICE'
